package com.example.firstproject.models;


public class NewNumber {

  Integer until;

  public NewNumber(Integer until) {
    this.until = until;
  }

  public NewNumber() {
  }

  public int getUntil() {
    return until;
  }

  public void setUntil(Integer until) {
    this.until = until;
  }

  @Override
  public String toString(){
    return "until : " + until.toString();
  }
}
